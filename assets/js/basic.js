$(document).ready(function(){
    
   $("#hidesignup").click(function(){
       
      $("#signupForm").hide();
      $("#loginForm").show();
       
   });
       
   $("#hidelogin").click(function(){
       
      $("#signupForm").show();
      $("#loginForm").hide();
       
   });
    
    
    
    
    
    
    
    
    
    
});